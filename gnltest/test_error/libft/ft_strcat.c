#include "includes/libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*pt1;
	char	*pt2;

	pt1 = s1;
	pt2 = (char *)s2;
	while (*pt1)
		pt1++;
	while (*pt2)
	{
		*pt1 = *pt2;
		pt1++;
		pt2++;
	}
	*pt1 = '\0';
	return (s1);
}
